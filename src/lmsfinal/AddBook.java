    private void btnSubmitStActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitStActionPerformed
        String sql="select * from book where accession=?";
        acc=txtAcc.getText();
        title=txtTitle.getText();
        author=txtAuthor.getText();
        isbn=txtIsbn.getText();
        edition=txtEdition.getText();
        bookself=txtSelf.getText();
        row=txtRow.getText();
        column=txtColumn.getText();
        //pin=txtPin.getText();
        ResultSet rs=null;
        PreparedStatement ps=null;
        String query=new String();
                        /////////////***Test Code**/////////////
        try{
            ps=conn.prepareStatement(sql);
            ps.setString(1, acc);
            rs=ps.executeQuery();
            if(rs.next()) {
                String access=rs.getString("accession");
                if(acc.compareTo(access)==0) {
                    JOptionPane.showMessageDialog(null, "This Book has already been Added!!");
                }
            }
            else{
                query="Insert into book values('"+acc+"','"+title+"','"+author+"','"+isbn+"','"+edition+"','"+bookself+"','"+row+"','"+column+"')";
                ps=conn.prepareStatement(query);
                ps.execute();
                JOptionPane.showMessageDialog(null,"Book successfully added to the Library!!");
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                ps.close();
            }catch(Exception e) {

            }
        }

    }//GEN-LAST:event_btnSubmitStActionPerformed

